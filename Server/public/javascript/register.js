document.addEventListener('DOMContentLoaded', function () {
    const stateSelect = document.getElementById('state');
    const districtSelect = document.getElementById('districtSelect');

    stateSelect.addEventListener('change', function () {
        const state = this.value;
        districtSelect.innerHTML = '<option value="">Select District</option>';
        if (allDistricts[state]) {
            allDistricts[state].forEach(function (district) {
                const opt = document.createElement('option');
                opt.value = district;
                opt.textContent = district;
                districtSelect.appendChild(opt);
            });
            districtSelect.disabled = false;
        } else {
            districtSelect.disabled = true;
        }
    });
});

document.addEventListener('DOMContentLoaded', function () {
    const pwd = document.getElementById('password');
    const cpwd = document.getElementById('confirm-password');
    const togglePwd = document.getElementById('togglePassword');
    const toggleCpwd = document.getElementById('toggleConfirmPassword');

    togglePwd.addEventListener('click', () => {
        const visible = pwd.type === 'text';
        pwd.type = visible ? 'password' : 'text';
        togglePwd.classList.toggle('fa-eye');
        togglePwd.classList.toggle('fa-eye-slash');
    });

    toggleCpwd.addEventListener('click', () => {
        const visible = cpwd.type === 'text';
        cpwd.type = visible ? 'password' : 'text';
        toggleCpwd.classList.toggle('fa-eye');
        toggleCpwd.classList.toggle('fa-eye-slash');
    });

    document.getElementById('specialization').onchange = function () {
        document.getElementById('otherSpecializationDiv').classList.toggle('hidden', this.value !== 'Other');
    };
    document.getElementById('affiliation').onchange = function () {
        document.getElementById('otherAffiliationDiv').classList.toggle('hidden', this.value !== 'Other');
    };

    document.getElementById('registrationForm').onsubmit = function (e) {
        const msgBox = document.getElementById('messageBox');
        if (pwd.value !== cpwd.value) {
            e.preventDefault();
            msgBox.textContent = 'Passwords do not match!';
            msgBox.className = 'message-box bg-red-500 text-white text-sm rounded px-4 py-2 mb-4';
            msgBox.classList.remove('hidden');
            return false;
        }
        // Handle "Other" for specialization
        const specialization = document.getElementById('specialization');
        if (specialization.value === 'Other') {
            let otherSpec = document.getElementById('otherSpecialization').value;
            let hiddenSpec = document.getElementById('hiddenSpecialization');
            if (!hiddenSpec) {
                hiddenSpec = document.createElement('input');
                hiddenSpec.type = 'hidden';
                hiddenSpec.name = 'specialization';
                hiddenSpec.id = 'hiddenSpecialization';
                this.appendChild(hiddenSpec);
            }
            hiddenSpec.value = otherSpec;
            specialization.disabled = true;
        } else {
            let hiddenSpec = document.getElementById('hiddenSpecialization');
            if (hiddenSpec) hiddenSpec.remove();
            specialization.disabled = false;
        }
        // Handle "Other" for affiliation
        const affiliation = document.getElementById('affiliation');
        if (affiliation.value === 'Other') {
            let otherAff = document.getElementById('otherAffiliation').value;
            let hiddenAff = document.getElementById('hiddenAffiliation');
            if (!hiddenAff) {
                hiddenAff = document.createElement('input');
                hiddenAff.type = 'hidden';
                hiddenAff.name = 'affiliation';
                hiddenAff.id = 'hiddenAffiliation';
                this.appendChild(hiddenAff);
            }
            hiddenAff.value = otherAff;
            affiliation.disabled = true;
        } else {
            let hiddenAff = document.getElementById('hiddenAffiliation');
            if (hiddenAff) hiddenAff.remove();
            affiliation.disabled = false;
        }
        msgBox.classList.add('hidden');
    };
});

window.handleGoogleCredentialResponse = async function (response) {
  try {
    const res = await fetch("/auth/google-login", {
      method: "POST",
      headers: {
        "Content-Type": "application/json",
      },
      body: JSON.stringify({ token: response.credential }),
    });

    const data = await res.json();

    if (res.ok && data.success) {
      
      const safeEmail = encodeURIComponent(data.email);
      window.location.href = `/dashboard/${safeEmail}`;
    } else {
      alert("Google Sign-In failed: " + (data.message || "Unknown error"));
    }
  } catch (err) {
    console.error("Google Sign-In failed:", err);
    alert("Google Sign-In failed. Please try again.");
  }
};
